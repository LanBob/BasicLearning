public class While {
    public static void main(String[] args) {
        int i = 0;
        while (i != 0) {
            i--;
            System.out.println("aa " + i);
        }

        int j = 0;
        do {
            j--;
            System.out.println(j);
        } while (j > 0);

    }
}
